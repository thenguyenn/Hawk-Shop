
package model;

public class HTTT {
    private int MaHTTT;
    private String TenHTTTT;

    public HTTT() {
    }

    public HTTT(int MaHTTT, String TenHTTTT) {
        this.MaHTTT = MaHTTT;
        this.TenHTTTT = TenHTTTT;
    }

    public int getMaHTTT() {
        return MaHTTT;
    }

    public void setMaHTTT(int MaHTTT) {
        this.MaHTTT = MaHTTT;
    }

    public String getTenHTTTT() {
        return TenHTTTT;
    }

    public void setTenHTTTT(String TenHTTTT) {
        this.TenHTTTT = TenHTTTT;
    }
    
    
}
