public class NhanVien1 {
   private int id;
   private String ten;
   private int id_role;
   private String email;
   private String SDT;
   private String diaChi;

    public NhanVien1() {
    }

    public NhanVien1(int id, String ten, int id_role, String email, String SDT, String diaChi) {
        this.id = id;
        this.ten = ten;
        this.id_role = id_role;
        this.email = email;
        this.SDT = SDT;
        this.diaChi = diaChi;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getId_role() {
        return id_role;
    }

    public void setId_role(int id_role) {
        this.id_role = id_role;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getSDT() {
        return SDT;
    }

    public void setSDT(String SDT) {
        this.SDT = SDT;
    }

    public String getDiaChi() {
        return diaChi;
    }

    public void setDiaChi(String diaChi) {
        this.diaChi = diaChi;
    }
   
}
    